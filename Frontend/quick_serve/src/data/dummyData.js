export const generateDummyToken = () => {
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
  let token = 'TKN';
  for (let i = 0; i < 13; i++) {
    token += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return token;
};

