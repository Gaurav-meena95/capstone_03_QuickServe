// Helper functions to get dummy data
export const getDummyShops = (filters = {}) => {
  let shops = [...dummyShops];
  
  if (filters.city) {
    shops = shops.filter(shop => shop.city.toLowerCase().includes(filters.city.toLowerCase()));
  }
  
  if (filters.category) {
    shops = shops.filter(shop => shop.category.toLowerCase().includes(filters.category.toLowerCase()));
  }
  
  if (filters.search) {
    shops = shops.filter(shop => 
      shop.name.toLowerCase().includes(filters.search.toLowerCase()) ||
      shop.description.toLowerCase().includes(filters.search.toLowerCase())
    );
  }
  
  return shops;
};

export const getDummyShopBySlug = (slug) => {
  const shop = dummyShops.find(shop => shop.slug === slug);
  if (!shop) return null;
  
  return {
    ...shop,
    categories: dummyCategories.map(category => ({
      ...category,
      menuItems: dummyMenuItems.filter(item => item.shopId === shop.id && item.categoryId === category.id)
    }))
  };
};

